    @Override
    public void moveFirst() {
        int currentIndex = tblSP.getSelectedRow();
        if (currentIndex == 0) {
            XDialog.alert( "Đã ở vị trí đầu tiên rồi!");
        } else {
            this.moveTo(0);
        }
    }

    @Override
    public void movePrevious() {
        this.moveTo(tblSP.getSelectedRow() - 1);
    }

    @Override
    public void moveNext() {
    this.moveTo(tblSP.getSelectedRow() + 1);
    }

    @Override
    public void moveLast() {
        this.moveTo(tblSP.getRowCount() - 1);
    }

    @Override
    public void moveTo(int index) {
         if (index < 0) {
        this.moveLast();
    } else if (index >= tblSP.getRowCount()) {
        this.moveFirst();
    } else {
        tblSP.clearSelection();
        tblSP.setRowSelectionInterval(index, index);
        this.edit();
    }
    }
