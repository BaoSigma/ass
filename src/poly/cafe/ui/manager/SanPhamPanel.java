     public void setQuyen() {
    if (XAuth.isViewer()) {
        // Phục vụ không được quyền gì
        btnADD.setEnabled(false);
        btnUpdate.setEnabled(false);
        btnDel.setEnabled(false);
        btnRead.setEnabled(true);
    } else if (XAuth.isStaff()) {
        // Nhân viên không được thêm/sửa/xóa nhân viên
        btnADD.setEnabled(false);
        btnUpdate.setEnabled(false);
        btnDel.setEnabled(false);
        btnRead.setEnabled(true);
    }else if (XAuth.isManager()) {
        // Nhân viên không được thêm/sửa/xóa nhân viên
        btnADD.setEnabled(true);
        btnUpdate.setEnabled(true);
        btnDel.setEnabled(true);
        btnRead.setEnabled(true);
    }
    // Quản lý thì không cần giới hạn
}
    @Override
    public SanPham getForm() {
        SanPham entity = new SanPham();
        entity.setGiaDU(Double.valueOf(txtGia.getText()));
        entity.setMaLSP(Integer.parseInt(txtLoai.getText()));
        entity.setTenDU(txtDU.getText());
        entity.setMaSP(lblSP.getText());
        return entity;
    }

    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblSP.getModel();
        model.setRowCount(0);
        items = dao.findAll();
        items.forEach(item -> {
            Object[] rowData = {
                item.getMaSP(),
                item.getMaLSP(),
                item.getTenDU(),
                item.getGiaDU(),
                false
            };
            model.addRow(rowData);
        });
    }

    @Override
    public void edit() {
        SanPham entity = items.get(tblSP.getSelectedRow());
        this.setForm(entity);
        this.setEditable(true);
    }

    @Override
    public void create() {
        if (XDialog.confirm("Bạn thực sự muốn thêm nhân viên này?")) {
        SanPham entity = this.getForm();
        dao.create(entity);
        this.fillToTable();
        this.clear();
    }
    }

    @Override
    public void update() {
        if (XDialog.confirm("Bạn thực sự muốn cập nhật nhân viên này?")) {
        SanPham entity = this.getForm();
        dao.update(entity);
        this.fillToTable();
        this.clear();
    }
    }

    @Override
    public void delete() {
        if (XDialog.confirm("Bạn thực sự muốn xóa?")) {
        String id = lblSP.getText();
        dao.deleteById(id);
        this.fillToTable();
        this.clear();
    }
    }

    @Override
    public void clear() {
        this.setForm(new SanPham());
    }

    @Override
    public void setEditable(boolean editable) {
        int rowCount = tblSP.getRowCount();
        int selectedRow = tblSP.getSelectedRow();
        String value = tblSP.getValueAt(selectedRow, 3).toString();
       
        if (value == null) {
            // Không có quyền gì hết
            lblSP.setEnabled(false);
            btnADD.setEnabled(false);
            btnUpdate.setEnabled(false);
            btnDel.setEnabled(false);
            btnMoveFirst.setEnabled(false);
            btnMovePrevious.setEnabled(false);
            btnMoveNext.setEnabled(false);
            btnMoveLast.setEnabled(false);
            return;
    }
    }
    private void on() {
        btnADD.setEnabled(true);
            btnUpdate.setEnabled(true);
            btnDel.setEnabled(true);
            btnMoveFirst.setEnabled(true);
            btnMovePrevious.setEnabled(true);
            btnMoveNext.setEnabled(true);
            btnMoveLast.setEnabled(true);
    }

    @Override
    public boolean checkAll() {

    SanPham entity = new SanPham();
    if (txtDU.getText().trim().isEmpty()) {
        XDialog.alert( "Vui lòng nhập đơn vị.");
        return false;
    }
    if (txtGia.getText().trim().isEmpty()) {
        XDialog.alert( "Vui lòng nhập giá.");
        return false;
    }

    if (txtLoai.getText().trim().isEmpty()) {
        XDialog.alert( "Vui lòng nhập loại sản phẩm.");
        return false;
    }

    return true;
    }

