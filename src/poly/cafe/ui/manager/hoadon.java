    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);
        items = dao.findAll();
        items.forEach(hd->{
            Object[] rowData ={
                hd.getMaHD(),
                hd.getNv(),
                hd.getGhiChu(),
                hd.getNgayTao(),
                hd.getThanhTien(),
                    false
            };
            model.addRow(rowData);
        });
    }

    @Override
    public void edit() {
        HoaDon entity = items.get(tblHoaDon.getSelectedRow());
        setForm(entity);
        setEditable(true);
    }

    @Override
    public void create() {
        HoaDon entity = getForm();
        dao.create(entity);
        fillToTable();
        clear(); 
    }

    @Override
    public void update() {
        HoaDon entity =this.getForm();
        dao.update(entity);
        this.fillToTable();
    }

    @Override
    public void delete() {
        if(XDialog.confirm("Bạn thực sự muốn xóa?")){
            String hd = txtHD.getText();
            dao.deleteById(hd);
            fillToTable();
            clear();
        }
    }

